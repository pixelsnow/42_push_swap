int	ft_isalnum(int c)
{
	unsigned char	uc;

	uc = (unsigned char)c;
	return ((uc >= '0' && uc <= '9') || (uc >= 'A' && uc <= 'Z')
		|| (uc >= 'a' && uc <= 'z'));
}
